    private void tambahKeranjangBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahKeranjangBtnActionPerformed
        // TODO add your handling code here:
        int selectedIndex = listPohon.getSelectedIndex();
        if (selectedIndex != -1) {
            String selectedTree = treeNames[selectedIndex];
            if (cartNames.contains(selectedTree)) {
                int index = cartNames.indexOf(selectedTree);
                cartQuantities.set(index, cartQuantities.get(index) + 1);
            } else {
                cartNames.add(selectedTree);
                cartQuantities.add(1);
            }
            updateCartTable();
        }
    }//GEN-LAST:event_tambahKeranjangBtnActionPerformed

