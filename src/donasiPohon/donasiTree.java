    private void hapusBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableKeranjang.getSelectedRow();
        if (selectedRow != -1) {
            String treeName = (String) tableKeranjang.getValueAt(selectedRow, 0);
            int index = cartNames.indexOf(treeName);
            cartNames.remove(index);
            cartQuantities.remove(index);
            updateCartTable();
        }
    }//GEN-LAST:event_hapusBtnActionPerformed

    private void tableKeranjangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKeranjangMouseClicked
        // TODO add your handling code here:
        int total = 0;
        for (int i = 0; i < cartNames.size(); i++) {
            int price = treePrices[listPohon.getNextMatch(cartNames.get(i), 0, Position.Bias.Forward)];
            total += price * cartQuantities.get(i);
        }
        totalHargaLbl.setText(String.valueOf(total));
    }//GEN-LAST:event_tableKeranjangMouseClicked

    private void updateCartTable() {
        DefaultTableModel model = (DefaultTableModel) tableKeranjang.getModel();
        model.setRowCount(0); // Clear the table
        for (int i = 0; i < cartNames.size(); i++) {
            model.addRow(new Object[]{cartNames.get(i), cartQuantities.get(i)});
        }
    }
    
    private void listPohonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listPohonMouseClicked
        // TODO add your handling code here:
        int selectedIndex = listPohon.getSelectedIndex();
        if (selectedIndex != -1) {
            namaLbl.setText("Nama: " + treeNames[selectedIndex]);
            hargaLbl.setText("Harga: " + treePrices[selectedIndex]);
            descLbl.setText("Deskripsi: " + treeDescriptions[selectedIndex]);
        }
    }//GEN-LAST:event_listPohonMouseClicked

