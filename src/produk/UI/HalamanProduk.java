class Produk {
        private String nama;
        private double harga;
        private int stok;
        private String deskripsi;

        public Produk(String nama, double harga, int stok, String deskripsi) {
            this.nama = nama;
            this.harga = harga;
            this.stok = stok;
            this.deskripsi = deskripsi;
        }

        public String getNama() {
            return nama;
        }

        public double getHarga() {
            return harga;
        }

        public int getStok() {
            return stok;
        }

        public String getDeskripsi() {
            return deskripsi;
        }

        @Override
        public String toString() {
            return nama; // Untuk menampilkan nama produk di dalam JList
        }
    }