    private void hapusProdukBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusProdukBtnActionPerformed
        // TODO add your handling code here:
        int selectedIndex = listKeranjang.getSelectedIndex();
        if (selectedIndex != -1) {
            String selectedNamaProduk = listKeranjang.getSelectedValue();
            keranjang.removeElementAt(selectedIndex);
            JOptionPane.showMessageDialog(this, "Produk berhasil dihapus");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih produk yang akan dihapus dari keranjang");
        }
    }//GEN-LAST:event_hapusProdukBtnActionPerformed

    private void addKeranjang_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addKeranjang_btnActionPerformed
        // TODO add your handling code here:
        int selectedIndex = listProduk.getSelectedIndex();
        if (selectedIndex != -1) {
            String selectedNamaProduk = listProduk.getSelectedValue();
            keranjang.addElement(selectedNamaProduk);
            JOptionPane.showMessageDialog(this, "Produk berhasil ditambahkan");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih produk yang akan ditambahkan ke keranjang");
        }
    }//GEN-LAST:event_addKeranjang_btnActionPerformed

