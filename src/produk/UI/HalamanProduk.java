    public HalamanProduk() {
        initComponents();
        listProduk.setModel(produk);
        listKeranjang.setModel(keranjang);
        
        // Tambahkan produk ke dalam list
        addProduk(new Produk("Produk A", 10000, 10, "Deskripsi Produk A"));
        addProduk(new Produk("Produk B", 20000, 20, "Deskripsi Produk B"));
        addProduk(new Produk("Produk C", 30000, 30, "Deskripsi Produk C"));
        addProduk(new Produk("Produk D", 40000, 40, "Deskripsi Produk D"));
        
        listProduk.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    String selectedNamaProduk = listProduk.getSelectedValue();
                    if (selectedNamaProduk != null) {
                        Produk selectedProduk = produkMap.get(selectedNamaProduk);
                        if (selectedProduk != null) {
                            namaProdukLbl.setText(selectedProduk.getNama());
                            hargaLbl.setText(String.valueOf(selectedProduk.getHarga()));
                            stokLbl.setText(String.valueOf(selectedProduk.getStok()));
                            deskripsiLbl.setText(selectedProduk.getDeskripsi());
                        }
                    }
                }
            }
        });
    }

    private void addProduk(Produk produkObj) {
        produk.addElement(produkObj.getNama());
        produkMap.put(produkObj.getNama(), produkObj);
        
    }
    
